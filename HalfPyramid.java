public class HalfPyramid {
    public static void main(String[] args) {
        int n = 5; // Number of rows
         // Outer loop for each row
        for (int i = 1; i <= n; i++) {
            // Inner loop for printing numbers
            for (int j = 1; j <= i; j++) {
                System.out.print(n);
            }
            System.out.println();
        }
    }

    
}
